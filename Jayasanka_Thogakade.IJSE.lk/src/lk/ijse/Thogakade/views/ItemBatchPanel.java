    private void handleEvents() {

        BatchTabel.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {

                if (BatchTabel.getSelectedRowCount() <= 0) {
                    return;
                }

                String customerId = dtmBatch.getValueAt(BatchTabel.getSelectedRow(), 0).toString();
                try {
                    batchDTO batch = batchController.search(customerId);

                    if (batch != null) {
                        txtItemrId.setText(batch.getItemCode());
                        txtBatch.setText(batch.getBatchID());
                        //txtExperDate.setText(batch.getBtchItemExperDate());
                       // txtMfdDate.setText(batch.getBatchItemMFDDate());
                    }
                    setEnable(true, btnSave,txtItemrId);
                    

                } catch (Exception ex) {
                    Logger.getLogger(AddCustomerFrom.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        });
    }
    
