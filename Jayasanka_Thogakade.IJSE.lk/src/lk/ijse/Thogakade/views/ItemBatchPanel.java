    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        Date date = jXDatePickerMfdDate.getDate();
        Date dateE = jXDatePickerExperDate.getDate();
        SimpleDateFormat d = new SimpleDateFormat("yyy-MM-dd");
        
        
        batchDTO BatchDTO = new batchDTO(
                txtItemrId.getText(),
                txtBatch.getText(),
                d.format(date),
                d.format(dateE));
        try {
            
            boolean result = false;
            if (txtItemrId.isEnabled()){
                result = batchController.add(BatchDTO);                
            }else{
                result = batchController.update(BatchDTO);
            }
            if (result) {
              
                loadData();
                BatchID();
                clearFields();
                setEnable(false);
                btnnewCustomer.requestFocusInWindow();
                JOptionPane.showMessageDialog(this, "Added success");
            } else {
                JOptionPane.showMessageDialog(this,
                        "Batch Deatil has been failed to add",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
                txtItemrId.requestFocusInWindow();
                txtItemrId.selectAll();
            }
        } catch (SQLException ex) {
            String errorMsg;


            switch (ex.getErrorCode()) {
                case 1062:
                    errorMsg = "Duplicate entry has been found. Please enter valid batcId to proceed.";
                    break;
                default:
                    errorMsg = "bach Detail has been failed to add";
                    break;
            }

            JOptionPane.showMessageDialog(this,
                    errorMsg,
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            txtItemrId.requestFocusInWindow();
            txtItemrId.selectAll();
        } catch (Exception ex) {
            Logger.getLogger(AddCustomerFrom.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
         String batchID = dtmBatch.getValueAt(BatchTabel.getSelectedRow(), 0).toString();
        try {
            boolean deleted = batchController.delete(batchID);
            if (deleted) {
               
                clearFields();
                setEnable(false);
                loadData();
                BatchTabel.clearSelection();
                BatchTabel.requestFocusInWindow();
                JOptionPane.showMessageDialog(this, "Deleted success");
            } else {
                JOptionPane.showMessageDialog(this,
                        "Batch detail deletion has been failed, Please try again.",
                        "Delete Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Batch deletion has been failed, Please try again.",
                    "Delete Error",
                    JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(AddCustomerFrom.class.getName()).log(Level.SEVERE, null, ex);
        }
                                      
    }//GEN-LAST:event_btnDeleteActionPerformed

