     private void loadData() {

        dtmBatch.setRowCount(0);

        try {
            ArrayList<batchDTO> allBatchs = batchController.getAll();

            if (allBatchs == null) {
                return;
            }

            for (batchDTO batch : allBatchs) {
                Object[] rowData = {
                    batch.getItemCode(),
                    batch.getBatchID(),
                    batch.getBtchItemExperDate(),
                    batch.getBtchItemExperDate(),
                };
                dtmBatch.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(AddCustomerFrom.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
