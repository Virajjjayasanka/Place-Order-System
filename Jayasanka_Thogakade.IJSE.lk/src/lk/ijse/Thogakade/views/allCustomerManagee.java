    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        dtmCustomer.setRowCount(0);
        String Date = txtDate.getText();
        String CustomerTelePoneNo = txtCustomerTelePoneNo.getText();
        String CustomerName = txtCustomerName.getText();
        String CustomerAddress = txtCustomerAddress.getText();
        String CustomerId = txtCustomerId.getText();

        CustomerDTO itemDTO = new CustomerDTO(Date, CustomerTelePoneNo, CustomerName, CustomerAddress, CustomerId);
        loadData();
        dtmCustomer = (DefaultTableModel) AllAddCustomerTabel.getModel();
        
        try {
            boolean isUpdate = ctrlCustomer.update(itemDTO);
            if (isUpdate) {
                JOptionPane.showMessageDialog(btnUpdate, "Make Type is updated");
            } else {
                JOptionPane.showMessageDialog(btnUpdate, "Make Type is not updated");
            }
        } catch (Exception ex) {
            Logger.getLogger(allCustomerManagee.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

