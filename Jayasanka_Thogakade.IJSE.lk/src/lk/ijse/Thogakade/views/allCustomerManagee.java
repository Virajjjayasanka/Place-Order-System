    //load tabel
    private void loadData() {

        dtmCustomer.setRowCount(0);

        try {
            ArrayList<CustomerDTO> allCustomers = ctrlCustomer.getAll();

            if (allCustomers == null) {
                return;
            }

            for (CustomerDTO customer : allCustomers) {
                Object[] rowData = {
                    customer.getId(),
                    customer.getName(),
                    customer.getAddress(),
                    customer.getTelePoneNO(),
                    customer.getDate(),
                };
                dtmCustomer.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(allCustomerManagee.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
