    private void txtSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSearchActionPerformed
        String customerID = txtSearch.getText();

        try {
            CustomerDTO customerDTO = ctrlCustomer.search(customerID);
            if (customerDTO == null) {
                JOptionPane.showMessageDialog(this, "No Member Found");
            } else {
                txtCustomerId.setText(customerDTO.getId());
                txtCustomerName.setText(customerDTO.getName());
                txtCustomerTelePoneNo.setText(customerDTO.getTelePoneNO());
                txtCustomerAddress.setText(customerDTO.getAddress());
                txtDate.setText(customerDTO.getDate());
               
            }
        } catch (Exception ex) {
            Logger.getLogger(allCustomerManagee.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtSearchActionPerformed


