    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            JasperReport comJasperReport = (JasperReport)JRLoader.loadObject(AddCustomerFrom.class.getResource("/lk/ijsejasper/report/CustomerReprt_A4.jasper"));
            JasperPrint filledReport = JasperFillManager.fillReport(comJasperReport, new HashMap<>(),new JRTableModelDataSource(AllAddCustomerTabel.getModel()));
            JFileChooser fileChooser = new JFileChooser();

            fileChooser.setAcceptAllFileFilterUsed(false);
            fileChooser.setFileFilter(new FileNameExtensionFilter("HTML Files('.html)", "html"));
            fileChooser.showSaveDialog(this);

            String filePath = fileChooser.getSelectedFile().getAbsolutePath();
            filePath+=".html";
            JasperExportManager.exportReportToHtmlFile(filledReport,filePath);

        } catch (JRException ex) {
            Logger.getLogger(AddCustomerFrom.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

